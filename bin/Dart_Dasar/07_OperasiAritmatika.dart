void main() {

  var bilangan1 = 10;
  var bilangan2 = 3;
  //                                                                          Operator Aritmatika
  print("$bilangan1 + $bilangan2 = ${bilangan1 + bilangan2}");            //        +
  print("$bilangan1 - $bilangan2 = ${bilangan1 - bilangan2}");            //        -
  print("$bilangan1 x $bilangan2 = ${bilangan1 * bilangan2}");            //        *
  print("$bilangan1 : $bilangan2 = ${bilangan1 / bilangan2} (Double)");   //        /
  print("$bilangan1 : $bilangan2 = ${bilangan1 ~/ bilangan2} (int)");     //        ~/
  print("$bilangan1 % $bilangan2 = ${bilangan1 % bilangan2} (modulus)");  //        %

}