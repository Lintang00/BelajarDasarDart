void main() {

//  Pada perulangan 'do while' akan mengeksekusi dahuli, kemudian baru mengecek kondisinya benar atau salah
  var counter = 100;

  do {
    print("Perulangan ke-$counter");

    counter++;
  }while(counter <= 10);
//  disini 100 <= 10 adalah salah, karena menggunakan perulangan 'do while'
//  maka mau salah atau benar akan dieksekusi dulu, kumudian pengecekan kondisinya

}