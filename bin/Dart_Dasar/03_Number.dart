void main() {

  int angka1 = 24;       // tipe 'int' untuk bilangan bulat(45)
  double angka2 = 3.4;   // tipe 'double' untuk bilangan desimal (0,5)

  print(angka1);
  print(angka2);

  // ======== Tipe num =======
    // tipe "num' ini seperti var, secara otomatis akan menentukan nilainya
    // sehingga bisa mengganti nilainya, tanpa memmbuat variabel baru
  num angka3 = 12;      // nilai 12 akan terbaca tipe data 'int'
  print(angka3);

  angka3 = 0.32;        // nilai 0.32 akan terbaca tipe data 'double'
  print(angka3);

}